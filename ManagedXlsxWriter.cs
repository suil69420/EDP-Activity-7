        private static string BuildWorksheetXml(IEnumerable<SheetRow> rows, bool includeDrawing, bool chartSheet)
        {
            var sb = new StringBuilder();
            sb.AppendLine(@"<?xml version=""1.0"" encoding=""UTF-8"" standalone=""yes""?>");
            sb.AppendLine($@"<worksheet xmlns=""{SpreadsheetNs}"" xmlns:r=""{RelationshipNs}"">");
            sb.AppendLine(@"  <sheetViews>");
            sb.AppendLine(@"    <sheetView workbookViewId=""0"">");
            sb.AppendLine(chartSheet
                ? @"      <pane ySplit=""4"" topLeftCell=""A5"" activePane=""bottomLeft"" state=""frozen"" />"
                : @"      <pane ySplit=""10"" topLeftCell=""A11"" activePane=""bottomLeft"" state=""frozen"" />");
            sb.AppendLine(@"    </sheetView>");
            sb.AppendLine(@"  </sheetViews>");
            sb.AppendLine(chartSheet ? BuildChartColumnsXml() : BuildReportColumnsXml());
            sb.AppendLine("  <sheetData>");

            foreach (var row in rows.OrderBy(r => r.RowNumber))
            {
                var height = row.Height > 0 ? $@" ht=""{row.Height}"" customHeight=""1""" : string.Empty;
                sb.AppendLine($@"    <row r=""{row.RowNumber}""{height}>");
                foreach (var cell in row.Cells.OrderBy(c => c.Column))
                {
                    var style = cell.StyleIndex > 0 ? $@" s=""{cell.StyleIndex}""" : string.Empty;
                    sb.AppendLine($@"      <c r=""{ColumnName(cell.Column)}{row.RowNumber}""{style} t=""inlineStr""><is><t xml:space=""preserve"">{Escape(cell.Value)}</t></is></c>");
                }
                sb.AppendLine("    </row>");
            }

            sb.AppendLine("  </sheetData>");
            if (!chartSheet)
            {
                sb.AppendLine(@"  <mergeCells count=""3"">");
                sb.AppendLine(@"    <mergeCell ref=""A1:F1"" />");
                sb.AppendLine(@"    <mergeCell ref=""A2:F2"" />");
                sb.AppendLine(@"    <mergeCell ref=""A7:F7"" />");
                sb.AppendLine(@"  </mergeCells>");
            }
            if (includeDrawing)
            {
                sb.AppendLine(@"  <drawing r:id=""rId1"" />");
            }
            sb.AppendLine("</worksheet>");
            return sb.ToString();
        }

