        public static void ExportReport(string outputPath, string reportTitle, DataTable reportData, DataTable chartData, string chartTitle, string logoIcoPath)
        {
            var logoBytes = LoadPngBytesFromIcon(logoIcoPath);
            var chartBytes = CreateChartImage(chartData, chartTitle);

            var sheet1Rows = BuildReportSheetRows(reportTitle, reportData);
            var sheet2Rows = BuildChartSheetRows(chartTitle, chartData);

            using (var fileStream = new FileStream(outputPath, FileMode.Create, FileAccess.Write, FileShare.None))
            using (var archive = new ZipArchive(fileStream, ZipArchiveMode.Create, false))
            {
                WriteEntry(archive, "[Content_Types].xml", BuildContentTypes());
                WriteEntry(archive, "_rels/.rels", BuildRootRels());
                WriteEntry(archive, "xl/workbook.xml", BuildWorkbookXml());
                WriteEntry(archive, "xl/_rels/workbook.xml.rels", BuildWorkbookRels());
                WriteEntry(archive, "xl/styles.xml", BuildStylesXml());
                WriteEntry(archive, "xl/worksheets/sheet1.xml", BuildWorksheetXml(sheet1Rows, true, false));
                WriteEntry(archive, "xl/worksheets/sheet2.xml", BuildWorksheetXml(sheet2Rows, true, true));
                WriteEntry(archive, "xl/worksheets/_rels/sheet1.xml.rels", BuildSheetRels("drawing1.xml"));
                WriteEntry(archive, "xl/worksheets/_rels/sheet2.xml.rels", BuildSheetRels("drawing2.xml"));
                WriteEntry(archive, "xl/drawings/drawing1.xml", BuildDrawingXml("rId1", 7, 0, 1_000_000, 1_000_000));
                WriteEntry(archive, "xl/drawings/drawing2.xml", BuildDrawingXml("rId1", 1, 1, 5_600_000, 3_200_000));
                WriteEntry(archive, "xl/drawings/_rels/drawing1.xml.rels", BuildDrawingRels("../media/image1.png"));
                WriteEntry(archive, "xl/drawings/_rels/drawing2.xml.rels", BuildDrawingRels("../media/image2.png"));
                WriteBytes(archive, "xl/media/image1.png", logoBytes);
                WriteBytes(archive, "xl/media/image2.png", chartBytes);
            }
        }

        private static List<SheetRow> BuildReportSheetRows(string reportTitle, DataTable data)
        {
            var rows = new List<SheetRow>();

            rows.Add(Row(1, 28, Cell(1, "VaccineCare Information System", 1)));
            rows.Add(Row(2, 20, Cell(1, "Generated report package", 7)));
            rows.Add(Row(4, 22, Cell(1, "Generated By:", 2), Cell(2, "____________________", 6)));
            rows.Add(Row(5, 22, Cell(1, "Signature:", 2), Cell(2, "____________________", 6)));
            rows.Add(Row(7, 26, Cell(1, reportTitle, 3)));
            rows.Add(Row(9, 24, Cell(1, "Report Data", 3)));

            var headerRow = new SheetRow { RowNumber = 10, Height = 24 };
            for (int column = 0; column < data.Columns.Count; column++)
            {
                headerRow.Cells.Add(Cell(column + 1, data.Columns[column].ColumnName, 4));
            }
            rows.Add(headerRow);

            for (int row = 0; row < data.Rows.Count; row++)
            {
                var sheetRow = new SheetRow { RowNumber = row + 11, Height = 22 };
                for (int column = 0; column < data.Columns.Count; column++)
                {
                    sheetRow.Cells.Add(Cell(column + 1, FormatValue(data.Rows[row][column]), 5));
                }
                rows.Add(sheetRow);
            }

            return rows;
        }

        private static List<SheetRow> BuildChartSheetRows(string chartTitle, DataTable chartData)
        {
            var rows = new List<SheetRow>
            {
                Row(1, 28, Cell(1, "Graph Visualization", 1)),
                Row(2, 22, Cell(1, chartTitle, 7)),
                Row(4, 24, Cell(1, chartData.Columns[0].ColumnName, 4), Cell(2, chartData.Columns[1].ColumnName, 4))
            };

            for (int row = 0; row < chartData.Rows.Count; row++)
            {
                rows.Add(Row(row + 5, 22, Cell(1, FormatValue(chartData.Rows[row][0]), 5), Cell(2, FormatValue(chartData.Rows[row][1]), 5)));
            }

            return rows;
        }

        private static SheetRow Row(int rowNumber, params SheetCell[] cells)
        {
            return Row(rowNumber, 0, cells);
        }

        private static SheetRow Row(int rowNumber, double height, params SheetCell[] cells)
        {
            var row = new SheetRow { RowNumber = rowNumber, Height = height };
            row.Cells.AddRange(cells);
            return row;
        }

        private static SheetCell Cell(int column, string value, int styleIndex = 0)
        {
            return new SheetCell { Column = column, Value = value ?? string.Empty, StyleIndex = styleIndex };
        }

        private static string FormatValue(object value)
        {
            if (value == null || value == DBNull.Value)
            {
                return string.Empty;
            }

            if (value is DateTime dateTime)
            {
                return dateTime.ToString("yyyy-MM-dd");
            }

            return Convert.ToString(value) ?? string.Empty;
        }

        private static byte[] LoadPngBytesFromIcon(string iconPath)
        {
            if (string.IsNullOrEmpty(iconPath) || !File.Exists(iconPath))
            {
                return CreatePlaceholderPng();
            }

            using (var icon = new Icon(iconPath))
            using (var bitmap = icon.ToBitmap())
            using (var stream = new MemoryStream())
            {
                bitmap.Save(stream, ImageFormat.Png);
                return stream.ToArray();
            }
        }

        private static byte[] CreatePlaceholderPng()
        {
            using (var bitmap = new Bitmap(1, 1))
            using (var stream = new MemoryStream())
            {
                bitmap.SetPixel(0, 0, Color.Transparent);
                bitmap.Save(stream, ImageFormat.Png);
                return stream.ToArray();
            }
        }

        private static byte[] CreateChartImage(DataTable chartData, string chartTitle)
        {
            using (var chart = new Chart())
            {
                chart.Width = 900;
                chart.Height = 420;
                chart.BackColor = Color.White;

                var chartArea = new ChartArea("Main");
                chartArea.BackColor = Color.White;
                chartArea.BorderColor = Color.FromArgb(214, 219, 224);
                chartArea.BorderWidth = 1;
                chartArea.AxisX.MajorGrid.LineColor = Color.FromArgb(230, 234, 239);
                chartArea.AxisY.MajorGrid.LineColor = Color.FromArgb(230, 234, 239);
                chartArea.AxisX.LabelStyle.Font = new Font("Segoe UI", 9f);
                chartArea.AxisY.LabelStyle.Font = new Font("Segoe UI", 9f);
                chartArea.AxisX.LineColor = Color.FromArgb(160, 168, 176);
                chartArea.AxisY.LineColor = Color.FromArgb(160, 168, 176);
                chart.ChartAreas.Add(chartArea);

                var series = new Series("Series")
                {
                    ChartType = SeriesChartType.Column,
                    IsValueShownAsLabel = true,
                    ChartArea = "Main",
                    Font = new Font("Segoe UI", 9f),
                    Color = Color.FromArgb(0, 102, 204),
                    BorderColor = Color.FromArgb(0, 86, 179)
                };

                foreach (DataRow row in chartData.Rows)
                {
                    var label = FormatValue(row[0]);
                    var value = ParseDouble(row[1]);
                    series.Points.AddXY(label, value);
                }

                chart.Series.Add(series);
                chart.Titles.Add(new Title(chartTitle, Docking.Top, new Font("Segoe UI", 14f, FontStyle.Bold), Color.FromArgb(33, 37, 41)));

                using (var stream = new MemoryStream())
                {
                    chart.SaveImage(stream, ChartImageFormat.Png);
                    return stream.ToArray();
                }
            }
        }

        private static double ParseDouble(object value)
        {
            if (value == null || value == DBNull.Value)
            {
                return 0;
            }

            double result;
            if (double.TryParse(Convert.ToString(value), out result))
            {
                return result;
            }

            return 0;
        }

        private static void WriteEntry(ZipArchive archive, string path, string content)
        {
            var entry = archive.CreateEntry(path, CompressionLevel.Optimal);
            using (var stream = new StreamWriter(entry.Open(), new UTF8Encoding(false)))
            {
                stream.Write(content);
            }
        }

        private static void WriteBytes(ZipArchive archive, string path, byte[] content)
        {
            var entry = archive.CreateEntry(path, CompressionLevel.Optimal);
            using (var stream = entry.Open())
            {
                stream.Write(content, 0, content.Length);
            }
        }

